
class outputMessage:

    def init(self,iouti,status,location,typeObj):
        self.iouti = iouti
        self.status = status
        self.location = location
        self.typeObj = typeObj
 
    def getIouti(self):
        return self.iouti
    
    def getStatus(self):
        return self.status

    def getLocation(self):
        return self.location

    def getTypeObj(self):
        return self.typeObj
                         
